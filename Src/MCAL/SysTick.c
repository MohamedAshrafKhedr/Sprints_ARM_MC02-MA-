/******************************************************************************
* \Syntax          : void SysTick_Init(uint32 Count)        
* \Description     : Initializing SysTick                                    
*                                                                             
* \Sync\Async      : ASynchronous                                               
* \Reentrancy      : Non Reentrant                                             
* \Parameters (in) : Count   Initial Counting Value                     
* \Parameters (out): None                                                      
* \Return value:   : void  E_OK
*                                    E_NOT_OK                                  
*******************************************************************************/
void SysTick_Init(uint32 Count)
{
	STRCL_REG = 0x00000003;
	STRELOAD_REG = Count;
	INCTRL_REG = INCTRL_TICK_INIT;
}

