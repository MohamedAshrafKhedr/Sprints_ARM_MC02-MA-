/******************************************************************************
* \Syntax          : enum Dio_LevelType Dio_ReadChannel(Dio_ChannelType ChannelId)        
* \Description     : returns the leveltype of the Dio_channel                                   
*                                                                             
* \Sync\Async      : Synchronous                                               
* \Reentrancy      : Reentrant                                             
* \Parameters (in) : ChannelId       The Id of the channel required to read its output                    
* \Parameters (out): None                                                      
* \Return value:   : enum Dio_LevelType  E_OK
*                                    E_NOT_OK                                  
*******************************************************************************/
enum Dio_LevelType Dio_ReadChannel(Dio_ChannelType ChannelId)
{
	return Bit_Banding(PORTF_BASE, ChannelId, GPIO_DATA_OFFSET);
	
}
void Dio_WriteChannel(Dio_ChannelType ChannelId, enum Dio_LevelType Level)
{
	Bit_Banding(PORTF_BASE, ChannelId, GPIO_DATA_OFFSET) = Level;
}
Dio_PortLevelType Dio_ReadPort(Dio_PortType PortId){
	
	return *((uint32 *)(PortId + GPIO_DATA_OFFSET));
}
void Dio_WritePort(Dio_PortType PortId, Dio_PortLevelType Level){
	
	int i;
	
	for(i = 0 ; i < 8; i++){
		Bit_Banding(PortId, i, GPIO_DATA_OFFSET) = Level;
	}
}
enum Dio_LevelType Dio_FlipChannel(Dio_ChannelType ChannelId){
	if(Dio_ReadChannel(ChannelId) == Low){
		Bit_Banding(PORTF_BASE, ChannelId, GPIO_DATA_OFFSET) = Hi;
	}else{
		Bit_Banding(PORTF_BASE, ChannelId, GPIO_DATA_OFFSET) = Low;
	}
	return Bit_Banding(PORTF_BASE, ChannelId, GPIO_DATA_OFFSET);
}
