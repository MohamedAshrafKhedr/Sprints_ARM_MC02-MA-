void LED_ON(uint8 time){
	if(0 == F){
		SysTick_Init(time * Freq);
		F = 1;
	}else{
		SysTick_Init(time * Freq);
	}
}
void LED_OFF(uint8 time){
	SysTick_Init(time * Freq);
}
void Sys_Tick_Notification(void){
	
	x = Dio_FlipChannel(F0);
	
	if(0 == (F%2)){
		LED_ON(2);
		F++;
	}else{
		LED_OFF(2);
		F++;
	}
	
}
